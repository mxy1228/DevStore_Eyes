package com.xmy.eyes.bean;

public class RequestLocateBean {

	private int type;
	private double lat;
	private double lng;
	private String city;
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public double getLat() {
		return lat;
	}
	public void setLat(double lat) {
		this.lat = lat;
	}
	public double getLng() {
		return lng;
	}
	public void setLng(double lng) {
		this.lng = lng;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	
	
}
